package com.ishrivasayush;

public class loops {
    public static void main(String ...args)
    {
        for(int i=1;i<10;i++)
        {
            System.out.println(i);
        }
        System.out.println("While Loop....");
        int i=1;
        while (i<10)
        {
            System.out.println(i);
            i++;
        }
        int j=1;
        System.out.println("Do While Loop....");
        do {
            System.out.println(j);
            j++;
        }while (j<1);
    }
}
